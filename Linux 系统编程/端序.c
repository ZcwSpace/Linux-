#include<stdio.h>

/*
大端序（Big-Endian，大尾序）：高位字节放在内存的低地址，低位字节放在内存的高地址。1=00000000 00000000 00000000 00000001

小端序（Little-Endian，小尾序）：低位字节放在内存的低地址，高位字节放在内存的高地址。1=10000000 00000000 00000000 00000000

端序是与硬件的体系结构相关而与所使用的操作系统无关的概念，目前基本上所有x86系列的PC机都是小端序。
*/

union c
{
    int a;
    char b;
}test;
      
 
 int main(void)

{
    test.a = 1;
    if (test.b == 1) 
    {
        printf("%d\n",1);
    } 
    else 
    {
        printf("%d\n",0);
    }
    //输出1->小端序
     
    return 0;
}